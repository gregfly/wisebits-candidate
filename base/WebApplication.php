    protected function resolveRequestUri(): string
    {
        if ($this->hasHeader('X-Rewrite-Url')) { // IIS
            $requestUri = $this->getHeader('X-Rewrite-Url');
        } elseif (isset($_SERVER['REQUEST_URI'])) {
            $requestUri = $_SERVER['REQUEST_URI'];
            if ($requestUri !== '' && $requestUri[0] !== '/') {
                $requestUri = preg_replace('/^(http|https):\/\/[^\/]+/i', '', $requestUri);
            }
        } elseif (isset($_SERVER['ORIG_PATH_INFO'])) { // IIS 5.0 CGI
            $requestUri = $_SERVER['ORIG_PATH_INFO'];
            if (!empty($_SERVER['QUERY_STRING'])) {
                $requestUri .= '?' . $_SERVER['QUERY_STRING'];
            }
        } else {
            $requestUri = '';
        }

        return trim($requestUri, '/'); // decoding url component
    }

    private $headers;

    public function getHeaders()
    {
        if ($this->headers === null) {
            if (function_exists('getallheaders')) {
                $this->headers = getallheaders();
            } elseif (function_exists('http_get_request_headers')) {
                $this->headers = http_get_request_headers();
            } else {
                $this->headers = [];
                foreach ($_SERVER as $name => $value) {
                    if (strncmp($name, 'HTTP_', 5) === 0) {
                        $name = str_replace(' ', '-', ucwords(strtolower(str_replace('_', ' ', substr($name, 5)))));
                        $this->headers[$name] = $value;
                    }
                }
            }
        }
        return $this->headers;
    }

    public function hasHeader($name)
    {
        return array_key_exists($name, $this->getHeaders());
    }

    public function getHeader($name, $default = null)
    {
        return $this->hasHeader($name)? $this->headers[$name] : $default;
    }

    public function getVersion(): string
    {
        if (isset($_SERVER['SERVER_PROTOCOL']) && $_SERVER['SERVER_PROTOCOL'] === 'HTTP/1.0') {
            return '1.0';
        } else {
            return '1.1';
        }
    }

    public function sendResponse($code = 200)
    {
        header('HTTP/' . $this->getVersion() . ' ' . $code . ' ' . self::$httpStatuses[$code]);
        exit(0);
    }
